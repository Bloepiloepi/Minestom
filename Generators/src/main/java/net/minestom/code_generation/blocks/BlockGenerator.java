package net.minestom.code_generation.blocks;

import com.google.gson.*;
import com.google.gson.stream.JsonReader;
import com.squareup.javapoet.*;
import net.minestom.code_generation.MinestomCodeGenerator;
import net.minestom.code_generation.util.NameUtil;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.lang.model.element.Modifier;
import java.io.File;
import java.io.FileNotFoundException;
import java.io.FileReader;
import java.util.*;

public final class BlockGenerator extends MinestomCodeGenerator {
    private static final Logger LOGGER = LoggerFactory.getLogger(BlockGenerator.class);
    private static final Gson GSON = new GsonBuilder().setPrettyPrinting().disableHtmlEscaping().create();
    private static final File DEFAULT_INPUT_FILE_BLOCKS = new File(DEFAULT_SOURCE_FOLDER_ROOT, "blocks.json");
    private final File blocksFile;
    private final File outputFolder;

    public BlockGenerator() {
        this(null, null);
    }

    public BlockGenerator(@Nullable File blocksFile) {
        this(blocksFile, null);
    }

    public BlockGenerator(@Nullable File blocksFile, @Nullable File outputFolder) {
        this.blocksFile = Objects.requireNonNullElse(blocksFile, DEFAULT_INPUT_FILE_BLOCKS);
        this.outputFolder = Objects.requireNonNullElse(outputFolder, DEFAULT_OUTPUT_FOLDER);
    }

    @Override
    public void generate() {
        if (!blocksFile.exists()) {
            LOGGER.error("Failed to find blocks.json.");
            LOGGER.error("Stopped code generation for blocks.");
            return;
        }
        if (!outputFolder.exists() && !outputFolder.mkdirs()) {
            LOGGER.error("Output folder for code generation does not exist and could not be created.");
            return;
        }
        // Important classes we use alot
        ClassName namespaceIDClassName = ClassName.get("net.minestom.server.utils", "NamespaceID");
        ClassName rawBlockDataClassName = ClassName.get("net.minestom.server.raw_data", "RawBlockData");
        ClassName rawBlockStateDataClassName = ClassName.get("net.minestom.server.raw_data", "RawBlockStateData");
        ClassName registryClassName = ClassName.get("net.minestom.server.registry", "Registry");

        List<JavaFile> filesToWrite = new ArrayList<>();

        JsonArray blocks;
        try {
            blocks = GSON.fromJson(new JsonReader(new FileReader(blocksFile)), JsonArray.class);
        } catch (FileNotFoundException e) {
            LOGGER.error("Failed to find blocks.json.");
            LOGGER.error("Stopped code generation for blocks.");
            return;
        }
        ClassName blockClassName = ClassName.get("net.minestom.server.instance.block", "Block");
        ClassName blockStateClassName = ClassName.get("net.minestom.server.instance.block", "BlockState");

        // Block
        TypeSpec.Builder blockClass = TypeSpec.classBuilder(blockClassName)
                .addSuperinterface(ClassName.get("net.kyori.adventure.key", "Keyed"))
                .addModifiers(Modifier.PUBLIC).addJavadoc("AUTOGENERATED");
        blockClass.addField(
                FieldSpec.builder(namespaceIDClassName, "id")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).addAnnotation(NotNull.class).build()
        );
        blockClass.addField(
                FieldSpec.builder(TypeName.SHORT, "defaultBlockState")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL).build()
        );
        blockClass.addField(
                FieldSpec.builder(ParameterizedTypeName.get(ClassName.get("java.util", "List"), blockStateClassName), "blockStates")
                        .initializer("new $T<>()", ClassName.get("java.util", "ArrayList"))
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class)
                        .build()
        );
        blockClass.addField(
                FieldSpec.builder(rawBlockDataClassName, "blockData")
                        .initializer("new $T()", rawBlockDataClassName)
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class)
                        .build()
        );
        // Block constructor
        blockClass.addMethod(
                MethodSpec.constructorBuilder()
                        .addParameter(ParameterSpec.builder(namespaceIDClassName, "id").addAnnotation(NotNull.class).build())
                        .addParameter(TypeName.SHORT, "defaultBlockState")

                        .addStatement("this.id = id")
                        .addStatement("this.defaultBlockState = defaultBlockState")
                        .addModifiers(Modifier.PROTECTED)
                        .build()
        );
        // Override key method (adventure)
        blockClass.addMethod(
                MethodSpec.methodBuilder("key")
                        .returns(ClassName.get("net.kyori.adventure.key", "Key"))
                        .addAnnotation(Override.class)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // addBlockState method
        blockClass.addMethod(
                MethodSpec.methodBuilder("addBlockState")
                        .addParameter(ParameterSpec.builder(blockStateClassName, "blockState").addAnnotation(NotNull.class).build())

                        .addStatement("this.blockStates.add(blockState)")
                        .addModifiers(Modifier.PUBLIC, Modifier.FINAL)
                        .build()
        );
        // getId method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getId")
                        .returns(namespaceIDClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getName method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getName")
                        .addAnnotation(NotNull.class)
                        .returns(ClassName.get(String.class))
                        .addStatement("return this.id.asString()")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getBlockId method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getBlockId")
                        .addAnnotation(Deprecated.class)
                        .returns(TypeName.SHORT)
                        .addStatement("return this.defaultBlockState")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getDefaultBlockStateId
        blockClass.addMethod(
                MethodSpec.methodBuilder("getDefaultBlockStateId")
                        .returns(TypeName.SHORT)
                        .addStatement("return this.defaultBlockState")
                        .addModifiers(Modifier.PUBLIC)
                        .build()

        );
        // getDefaultBlockState
        blockClass.addMethod(
                MethodSpec.methodBuilder("getDefaultBlockState")
                        .returns(blockStateClassName)
                        .addStatement("return $T.BLOCK_STATE_REGISTRY.get(defaultBlockState)", registryClassName)
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getBlockStates method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getBlockStates")
                        .returns(ParameterizedTypeName.get(ClassName.get(List.class), blockStateClassName))
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.blockStates")
                        .addModifiers(Modifier.PUBLIC, Modifier.FINAL) // Should this be final, for now I guess so.
                        .build()
        );
        // getBlockData method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getBlockData")
                        .returns(rawBlockDataClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.blockData")
                        .addModifiers(Modifier.PUBLIC, Modifier.FINAL)
                        .build()
        );
        // isAir method
        blockClass.addMethod(
                MethodSpec.methodBuilder("isAir")
                        .returns(TypeName.BOOLEAN)
                        // UPDATE: Make sure they didn't add new AIR types.
                        .addStatement("return this != AIR && this != VOID_AIR && this != CAVE_AIR")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // isLiquid method
        blockClass.addMethod(
                MethodSpec.methodBuilder("isLiquid")
                        .returns(TypeName.BOOLEAN)
                        // UPDATE: Make sure they didn't add new liquids.
                        .addStatement("return this != WATER && this != LAVA")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // hasBlockEntity method
        blockClass.addMethod(
                MethodSpec.methodBuilder("hasBlockEntity")
                        .returns(TypeName.BOOLEAN)
                        .addStatement("return $T.BLOCKS.contains(this)", ClassName.get("net.minestom.server.instance.block", "BlockEntity"))
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getAlternative method
        blockClass.addMethod(
                MethodSpec.methodBuilder("getAlternative")
                        .returns(blockStateClassName)
                        .addParameter(TypeName.SHORT, "stateId")
                        .addAnnotation(Nullable.class)
                        .addAnnotation(Deprecated.class)
                        .addStatement("return getBlockState(stateId)")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // withProperties method
        blockClass.addMethod(
                MethodSpec.methodBuilder("withProperties")
                        .returns(TypeName.SHORT)
                        .addParameter(
                                ParameterSpec.builder(ArrayTypeName.of(String.class), "properties").addAnnotation(NotNull.class).build()
                        ).varargs()

                        .beginControlFlow("for ($T state : blockStates)", blockStateClassName)
                        .beginControlFlow("if ($T.equals(state.getProperties(), properties))", ClassName.get("java.util", "Arrays"))
                        .addStatement("return state.getId()")
                        .endControlFlow()
                        .endControlFlow()
                        .addStatement("return this.defaultBlockState")
                        .addModifiers(Modifier.PUBLIC)

                        .build()
        );
        // getBlockState
        blockClass.addMethod(
                MethodSpec.methodBuilder("getBlockState")
                        .returns(blockStateClassName)
                        .addParameter(TypeName.SHORT, "stateId")
                        .addAnnotation(Nullable.class)
                        .addCode(
                                CodeBlock.builder()
                                        .beginControlFlow("for ($T state : blockStates)", blockStateClassName)
                                        .beginControlFlow("if (state.getId() == stateId)")
                                        .addStatement("return state")
                                        .endControlFlow()
                                        .endControlFlow()
                                        .addStatement("return null")
                                        .build()
                        )
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // fromStateId Method
        blockClass.addMethod(
                MethodSpec.methodBuilder("fromStateId")
                        .returns(blockClassName)
                        .addAnnotation(NotNull.class)
                        .addParameter(TypeName.SHORT, "id")
                        .addStatement("return $T.BLOCK_STATE_REGISTRY.get(id).getBlock()", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // getNumericalId
        blockClass.addMethod(
                MethodSpec.methodBuilder("getNumericalId")
                        .returns(TypeName.INT)
                        .addStatement("return $T.BLOCK_REGISTRY.getId(this)", registryClassName)
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // fromId Method
        blockClass.addMethod(
                MethodSpec.methodBuilder("fromId")
                        .returns(blockClassName)
                        .addAnnotation(NotNull.class)
                        .addParameter(TypeName.INT, "id")
                        .addStatement("return $T.BLOCK_REGISTRY.get((short) id)", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // fromId Method
        blockClass.addMethod(
                MethodSpec.methodBuilder("fromId")
                        .returns(blockClassName)
                        .addAnnotation(NotNull.class)
                        .addParameter(ClassName.get("net.kyori.adventure.key", "Key"), "id")
                        .addStatement("return $T.BLOCK_REGISTRY.get(id)", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // toString method
        blockClass.addMethod(
                MethodSpec.methodBuilder("toString")
                        .addAnnotation(NotNull.class)
                        .addAnnotation(Override.class)
                        .returns(String.class)
                        // this resolves to [Namespace]
                        .addStatement("return \"[\" + this.id + \"]\"")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // values method
        blockClass.addMethod(
                MethodSpec.methodBuilder("values")
                        .addAnnotation(NotNull.class)
                        .returns(ParameterizedTypeName.get(ClassName.get(List.class), blockClassName))
                        .addStatement("return $T.BLOCK_REGISTRY.values()", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // BlockState
        TypeSpec.Builder blockStateClass = TypeSpec.classBuilder(blockStateClassName)
                .addSuperinterface(ClassName.get("net.kyori.adventure.key", "Keyed"))
                .addModifiers(Modifier.PUBLIC)
                .addJavadoc("AUTOGENERATED");
        blockStateClass.addField(
                FieldSpec.builder(namespaceIDClassName, "namespaceId")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class)
                        .build()
        );
        blockStateClass.addField(
                FieldSpec.builder(TypeName.SHORT, "id")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .build()
        );
        blockStateClass.addField(
                FieldSpec.builder(blockClassName, "block")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class).build()
        );
        blockStateClass.addField(
                FieldSpec.builder(rawBlockStateDataClassName, "blockStateData")
                        .initializer("new $T()", rawBlockStateDataClassName)
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class)
                        .build()
        );
        blockStateClass.addField(
                FieldSpec.builder(ArrayTypeName.of(String.class), "properties")
                        .addModifiers(Modifier.PRIVATE, Modifier.FINAL)
                        .addAnnotation(NotNull.class)
                        .build()
        );
        // AIR_0 static field
        blockStateClass.addField(
                FieldSpec.builder(blockStateClassName, "AIR_0")
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                        .initializer("$T.AIR_0", ClassName.get("net.minestom.server.instance.block.states", "Air"))
                        .build()
        );
        // BlockState constructor
        blockStateClass.addMethod(
                MethodSpec.constructorBuilder()
                        .addParameter(ParameterSpec.builder(namespaceIDClassName, "namespaceId").addAnnotation(NotNull.class).build())
                        .addParameter(TypeName.SHORT, "id")
                        .addParameter(ParameterSpec.builder(blockClassName, "block").addAnnotation(NotNull.class).build())
                        .addParameter(
                                ParameterSpec.builder(ArrayTypeName.of(String.class), "properties").addAnnotation(NotNull.class).build()
                        )
                        .varargs()

                        .addStatement("this.namespaceId = namespaceId")
                        .addStatement("this.id = id")
                        .addStatement("this.block = block")
                        .addStatement("this.properties = properties")

                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getNamespaceId method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getNamespaceId")
                        .addAnnotation(NotNull.class)
                        .returns(namespaceIDClassName)
                        .addStatement("return this.namespaceId")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // Override key method (adventure)
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("key")
                        .returns(ClassName.get("net.kyori.adventure.key", "Key"))
                        .addAnnotation(Override.class)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.namespaceId")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getId method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getId")
                        .returns(TypeName.SHORT)
                        .addStatement("return this.id")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getBlock method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getBlock")
                        .returns(blockClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.block")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getProperties method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getProperties")
                        .returns(ArrayTypeName.of(String.class))
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.properties")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // createPropertiesMap
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("createPropertiesMap")
                        .returns(ParameterizedTypeName.get(Map.class, String.class, String.class))
                        .addAnnotation(NotNull.class)

                        .addStatement(
                                "$T map = new $T<>()",
                                ParameterizedTypeName.get(Map.class, String.class, String.class),
                                HashMap.class
                        )
                        .beginControlFlow("for (String p : this.properties)")
                        .addStatement("String[] parts = p.split($S)", "=")
                        .addStatement("map.put(parts[0], parts[1])")
                        .endControlFlow()
                        .addStatement("return map")

                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // getProperty method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getProperty")
                        .addAnnotation(Nullable.class)
                        .returns(String.class)
                        .addParameter(ParameterSpec.builder(String.class, "key").addAnnotation(NotNull.class).build())

                        .beginControlFlow("for (String p : this.properties)")
                        .addStatement("String[] parts = p.split($S)", "=")
                        .beginControlFlow("if (parts.length > 1 && parts[0].equals(key))")
                        .addStatement("return parts[1]")
                        .endControlFlow()
                        .endControlFlow()
                        .addStatement("return null")

                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // fromId Method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("fromId")
                        .returns(blockStateClassName)
                        .addAnnotation(NotNull.class)
                        .addParameter(TypeName.INT, "id")
                        .addStatement("return $T.BLOCK_STATE_REGISTRY.get((short) id)", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // fromId Method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("fromId")
                        .returns(blockStateClassName)
                        .addAnnotation(NotNull.class)
                        .addParameter(ClassName.get("net.kyori.adventure.key", "Key"), "id")
                        .addStatement("return $T.BLOCK_STATE_REGISTRY.get(id)", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // getBlockData method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("getBlockStateData")
                        .returns(rawBlockStateDataClassName)
                        .addAnnotation(NotNull.class)
                        .addStatement("return this.blockStateData")
                        .addModifiers(Modifier.PUBLIC, Modifier.FINAL)
                        .build()
        );
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("isSolid")
                        .returns(TypeName.BOOLEAN)
                        .addStatement("return this.blockStateData.solid")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("isLiquid")
                        .returns(TypeName.BOOLEAN)
                        .addStatement("return this.blockStateData.liquid")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // values method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("values")
                        .addAnnotation(NotNull.class)
                        .returns(ParameterizedTypeName.get(ClassName.get(List.class), blockStateClassName))
                        .addStatement("return $T.BLOCK_STATE_REGISTRY.values()", registryClassName)
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .build()
        );
        // toString method
        blockStateClass.addMethod(
                MethodSpec.methodBuilder("toString")
                        .addAnnotation(NotNull.class)
                        .addAnnotation(Override.class)
                        .returns(String.class)
                        // this resolves to [Namespace]
                        .addStatement("return \"[\" + this.id + \",\"+ this.namespaceId + \"]\"")
                        .addModifiers(Modifier.PUBLIC)
                        .build()
        );
        // For static initStates
        CodeBlock.Builder staticBlock = CodeBlock.builder();
        CodeBlock.Builder staticBlock2 = CodeBlock.builder();
        // Use data
        for (JsonElement b : blocks) {
            JsonObject block = b.getAsJsonObject();

            String blockName = block.get("name").getAsString();

            blockClass.addField(
                    FieldSpec.builder(
                            blockClassName,
                            blockName
                    ).initializer(
                            "new $T($T.from($S), (short) $L)",
                            blockClassName,
                            namespaceIDClassName,
                            block.get("id").getAsString(),
                            block.get("defaultBlockState").getAsInt()
                    ).addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL).build()
            );

            JsonArray states = block.get("states").getAsJsonArray();
            // States should be split into a maximum size of maxSize to not reach the java limit.
            // If we ever reach the java limit we can reduce this number, it does not change anything users should be using.
            int maxSize = 800;

            for (int i=0; i < Math.ceil(states.size() / (double) maxSize); i++) {
                String suffix = "";
                if (i > 0) {
                    suffix = String.valueOf(i + 1); // RedstoneWire, RedstoneWire2, RedstoneWire 3
                }
                ClassName blockStateSpecificClassName = ClassName.get(
                        "net.minestom.server.instance.block.states",
                        NameUtil.convertSnakeCaseToCamelCase(blockName.toLowerCase()) + suffix
                );

                // Common blockStateSpecificClass structure
                TypeSpec.Builder blockStateSpecificClass = TypeSpec.classBuilder(blockStateSpecificClassName)
                        .addAnnotation(
                                AnnotationSpec.builder(Deprecated.class)
                                        .addMember("since", "$S", "forever")
                                        .addMember("forRemoval", "$L", false).build()
                        )
                        .addModifiers(Modifier.PUBLIC, Modifier.FINAL).addJavadoc("AUTOGENERATED");

                // initStates method
                MethodSpec.Builder initStatesMethod = MethodSpec.methodBuilder("initStates")
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC);

                CodeBlock.Builder internalStaticBlock = CodeBlock.builder();


                for (int j = i*maxSize; j < Math.min((i+1)*maxSize, states.size()); j++) { // from 0 until maxSize or the rest of the array.
                    JsonObject state = states.get(j).getAsJsonObject();
                    String stateName = blockName + "_" + j;

                    // block state properties
                    JsonObject properties = state.get("properties").getAsJsonObject();
                    // if not empty
                    if (properties.size() != 0) {
                        StringBuilder propertiesStr = new StringBuilder();
                        // has properties
                        for (Map.Entry<String, JsonElement> entry : properties.entrySet()) {
                            String key = entry.getKey();
                            String value = entry.getValue().getAsString();
                            propertiesStr.append('"').append(key).append('=').append(value).append("\",");
                        }
                        // Delete last comma
                        propertiesStr.deleteCharAt(propertiesStr.length() - 1);

                        blockStateSpecificClass.addField(
                                FieldSpec.builder(blockStateClassName, stateName)
                                        .initializer(
                                                "new $T($T.from($S), (short) $L, $T.$N, **REPLACE**)"
                                                        .replace("**REPLACE**", propertiesStr.toString()),
                                                blockStateClassName,
                                                namespaceIDClassName,
                                                block.get("id").getAsString() + "_" + j, // minecraft:stone_0
                                                state.get("id").getAsString(),
                                                blockClassName,
                                                blockName
                                        )
                                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                                        .build()
                        );
                    } else {
                        // has no properties
                        blockStateSpecificClass.addField(
                                FieldSpec.builder(blockStateClassName, stateName)
                                        .initializer(
                                                "new $T($T.from($S), (short) $L, $T.$N)",
                                                blockStateClassName,
                                                namespaceIDClassName,
                                                block.get("id").getAsString() + "_" + j, // minecraft:stone_0
                                                state.get("id").getAsString(),
                                                blockClassName,
                                                blockName
                                        )
                                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                                        .build()
                        );
                    }


                    // Add field to initStates method
                    initStatesMethod.addStatement(
                            "Block.$N.addBlockState($N)",
                            blockName,
                            stateName
                    );
                    // Add field to internal static method
                    internalStaticBlock.addStatement("$T.BLOCK_STATE_REGISTRY.register($N)", registryClassName, stateName);
                }
                // Add initStates Method
                blockStateSpecificClass.addMethod(initStatesMethod.build());
                blockStateSpecificClass.addStaticBlock(internalStaticBlock.build());

                // Add initStates method refence to static block
                staticBlock.addStatement("$T.initStates()", blockStateSpecificClassName);

                // Add BlockStates to list of files we need to write:
                filesToWrite.add(JavaFile.builder("net.minestom.server.instance.block.states", blockStateSpecificClass.build()).build());
            }
            // Add to static init.
            staticBlock2.addStatement("$T.BLOCK_REGISTRY.register($N)", registryClassName, blockName);
        }
        blockClass.addStaticBlock(staticBlock.build());
        blockClass.addStaticBlock(staticBlock2.build());
        // Add ignore deprecations annotation
        blockClass.addAnnotation(AnnotationSpec.builder(SuppressWarnings.class).addMember("value", "$S", "deprecation").build());


        // Add Block & BlockState to list of files we need to write:
        filesToWrite.add(JavaFile.builder("net.minestom.server.instance.block", blockClass.build()).build());
        filesToWrite.add(JavaFile.builder("net.minestom.server.instance.block", blockStateClass.build()).build());

        // Write files to outputFolder
        writeFiles(filesToWrite, outputFolder);
    }
}
